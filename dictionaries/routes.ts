export const getLocalizedUrl = (physicalPath: string, lang: string): string => {
  const segments = physicalPath.split('/').filter(Boolean);
  const translatedSegments = segments.map(segment => {
    return routeDictionary[segment]?.[lang] || segment;
  });
  return `/${lang}/${translatedSegments.join('/')}`;
};

export const getDocumentPath = (
  fileUrlOrLang: string,
  filePathOrUndefined?: string,
  lang?: string
): string => {
  if (!filePathOrUndefined) {
    return fileUrlOrLang;
  }
  return `/assets/documents/${fileUrlOrLang}/${filePathOrUndefined}`;
};
